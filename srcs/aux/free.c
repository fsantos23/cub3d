#include "../../inc/cub3d.h"

void	free_matriz(char ***mrtz)
{
	int	i;

	i = 0;
	if (*mrtz)
	{
		while ((*mrtz)[i])
		{
			free((*mrtz)[i]);
			i++;
		}
	}
	free(*mrtz);
	*mrtz = NULL;
}
